    @property({ type: Node, tooltip: "content" })
    protected content: Node;
    @property({ type: Node, displayName: "nd_back" })
    nd_back: Node;

    private Index = 0;

    protected onAfterOpen(): void {
        this.nd_back.on(NodeEventType.TOUCH_END, this.onBack, this);

        this.Index = 0
        this.content.destroyAllChildren()
        this.schedule(this.setLab)
    }

    protected setLab() {
        let res = this.TEXT[this.Index++]
        if (!res)
            return this.unscheduleAllCallbacks()

        let node = new Node()
        let Transform = node.addComponent(UITransform)
        Transform.width = 650;

        let Lab = node.addComponent(Label)

        if (res.horizontalAlign)
            Lab.horizontalAlign = res.horizontalAlign
        else
            Lab.horizontalAlign = HorizontalTextAlignment.LEFT
        Lab.verticalAlign = VerticalTextAlignment.CENTER
        Lab.fontSize = 28
        Lab.lineHeight = 40
        Lab.enableWrapText = true
        Lab.useSystemFont = false

        Lab.overflow = Overflow.RESIZE_HEIGHT;
        Lab.isBold = res.bold

       

        Lab.string = res.text

        this.content.addChild(node)
    }
